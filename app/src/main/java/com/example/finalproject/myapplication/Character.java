    public static int[] realStats(int index) {
        int[] stats = new int[6];
        for (int i = 0; i < 6; i++) {
            stats[i] = Integer.parseInt(pokemons[index][i + 2]);
        }
        return stats;
    }

    // get pokemon's stats in 0-5 ratings
    public static int[] getStats(int index) {
        int[] stats = new int[6];
        // convert hp, atk, def, satk and sdef into 0-5 ratings
        for (int i = 0; i < 5; i++) {
            int value = realStats(index)[i];
            if (value >= 0 && value <= 49) {
                stats[i] = 0;
            } else if (value <= 74) {
                stats[i] = 1;
            } else if (value <= 99) {
                stats[i] = 2;
            } else if (value <= 124) {
                stats[i] = 3;
            } else if (value <= 149) {
                stats[i] = 4;
            } else {
                stats[i] = 5;
            }
        }
        // convert speed into 0-5 ratings.
        int value = realStats(index)[5];
        if (value >= 0 && value <= 29) {
            stats[5] = 0;
        } else if (value <= 54) {
            stats[5] = 1;
        } else if (value <= 79) {
            stats[5] = 2;
        } else if (value <= 104) {
            stats[5] = 3;
        } else if (value <= 129) {
            stats[5] = 4;
        } else {
            stats[5] = 5;
        }
        return stats;
    }

